#include "minishell.h"

void	echo(t_cmd *cmd)
{
	int	i;

	i = 1;
	if (!ft_strcmp(cmd->args[1], "-n"))
		i++;
	while (cmd->args[i])
		printf("%s", cmd->args[i++]);
	if (ft_strcmp(cmd->args[1], "-n"))
		printf("\n");
	exit(0);
}
